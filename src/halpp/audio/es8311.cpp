static const CoeffDiv* get_coeff(uint32_t mclk, uint32_t rate) {
  for (const auto& coeff : coeff_table) {
    if (coeff.rate == rate && coeff.mclk == mclk) {
      return &coeff;
    }
  }
  return nullptr;
}

